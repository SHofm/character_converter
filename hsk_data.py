def get_hsk_level(word):
    """Get HSK level for a word, returns (level, frequency) or (0, 5) if not found"""
    if word in HSK_VOCABULARY:
        return HSK_VOCABULARY[word]
    # Try to find partial matches for compound words
    for char in word:
        if char in HSK_VOCABULARY:
            return HSK_VOCABULARY[char]
    return (0, 5)  # Unknown level

def get_character_breakdown(word):
    """Get individual character meanings for a compound word"""
    if len(word) <= 1:
        return None
    
    breakdown = []
    for char in word:
        if char in CHARACTER_MEANINGS:
            breakdown.append((char, CHARACTER_MEANINGS[char]))
        else:
            breakdown.append((char, "?"))
    
    return breakdown if breakdown else None

def get_hsk_color(level):
    """Get color for HSK level display"""
    colors = {
        1: "#4CAF50",  # Green - Beginner
        2: "#8BC34A",  # Light Green - Elementary
        3: "#FFC107",  # Amber - Intermediate
        4: "#FF9800",  # Orange - Upper Intermediate
        5: "#FF5722",  # Deep Orange - Advanced
        6: "#F44336",  # Red - Mastery
        0: "#9E9E9E",  # Gray - Unknown
    }
    return colors.get(level, "#9E9E9E")

def get_frequency_label(freq):
    """Get frequency label"""
    labels = {
        1: "Very Common",
        2: "Common",
        3: "Moderate",
        4: "Less Common",
        5: "Rare",
    }
    return labels.get(freq, "Unknown")
